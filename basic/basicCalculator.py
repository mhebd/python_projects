# Basic calculator for two number

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print("{0} + {1} = {2}".format(num1, num2, (num1 + num2)))
print("{0} - {1} = {2}".format(num1, num2, (num1 - num2)))
print("{0} X {1} = {2}".format(num1, num2, (num1 * num2)))
print("{0} / {1} = {2}".format(num1, num2, (num1 / num2)))
